// O(n)
// intスライスの差分配列を返す
func DiffArray(sl []int) []int {
	res := make([]int, 0, len(sl))
	res = append(res, sl[0])
	for i := 1; i < len(sl); i++ {
		res = append(res, sl[i]-sl[i-1])
	}
	return res
}

// O(n)
// 一次元累積和を返す（index0には0を入れる。）
func PrefixSum(sl []int) []int {
	n := len(sl)
	res := make([]int, n+1)
	for i := 0; i < n; i++ {
		res[i+1] = res[i] + sl[i]
	}
	return res
}

