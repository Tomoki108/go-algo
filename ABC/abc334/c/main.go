// O(n)
// intスライスの差分配列を返す
func DiffArray(sl []int) []int {
	res := make([]int, 0, len(sl))
	res = append(res, sl[0])
	for i := 1; i < len(sl); i++ {
		res = append(res, sl[i]-sl[i-1])
	}
	return res
}

